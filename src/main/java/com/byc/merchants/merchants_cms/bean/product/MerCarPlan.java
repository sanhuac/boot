import java.io.Serializable;

/**
 * @author pht
 * @program merchants_cms
 * @date 2018/9/18 0018
 */
public class MerCarPlan implements Serializable {
    private static final long serialVersionUID = -3240873615203297764L;

    private Integer plan_id;//'分期方案id',
    private Integer car_id; //'车辆id',
    private Double down_pay;//首付价格',
    private Double monthly_pay;//'月付',
    private Integer term;//'分期期数',
    private Integer plan_status; //'方案状态[0:不删除   1:删除]',

    public Integer getPlan_id() {
        return plan_id;
    }

    public void setPlan_id(Integer plan_id) {
        this.plan_id = plan_id;
    }

    public Integer getCar_id() {
        return car_id;
    }

    public void setCar_id(Integer car_id) {
        this.car_id = car_id;
    }

    public Double getDown_pay() {
        return down_pay;
    }

    public void setDown_pay(Double down_pay) {
        this.down_pay = down_pay;
    }

    public Double getMonthly_pay() {
        return monthly_pay;
    }

    public void setMonthly_pay(Double monthly_pay) {
        this.monthly_pay = monthly_pay;
    }

    public Integer getTerm() {
        return term;
    }

    public void setTerm(Integer term) {
        this.term = term;
    }

    public Integer getPlan_status() {
        return plan_status;
    }

    public void setPlan_status(Integer plan_status) {
        this.plan_status = plan_status;
    }

    @Override
    public String toString() {
        return "MerCarPlan{" +
                "plan_id=" + plan_id +
                ", car_id=" + car_id +
                ", down_pay=" + down_pay +
                ", monthly_pay=" + monthly_pay +
                ", term=" + term +
                ", plan_status=" + plan_status +
                '}';
    }
}
