import java.io.Serializable;

/**
 * @author pht
 * @program merchants_cms
 * @date 2018/9/18 0018
 */
public class CarImage implements Serializable {
    private static final long serialVersionUID = -6140851299630979332L;

    private Integer image_id; // '图片id',
    private Integer car_id; //'车辆id',
    private String image_url; //'图片短连接',
    private Integer image_status;//'图片状态[0:不删除  1:删除]',
    private Integer create_time;//'上传时间',

    public Integer getImage_id() {
        return image_id;
    }

    public void setImage_id(Integer image_id) {
        this.image_id = image_id;
    }

    public Integer getCar_id() {
        return car_id;
    }

    public void setCar_id(Integer car_id) {
        this.car_id = car_id;
    }

    public String getImage_url() {
        return image_url;
    }

    public void setImage_url(String image_url) {
        this.image_url = image_url;
    }

    public Integer getImage_status() {
        return image_status;
    }

    public void setImage_status(Integer image_status) {
        this.image_status = image_status;
    }

    public Integer getCreate_time() {
        return create_time;
    }

    public void setCreate_time(Integer create_time) {
        this.create_time = create_time;
    }

    @Override
    public String toString() {
        return "CarImage{" +
                "image_id=" + image_id +
                ", car_id=" + car_id +
                ", image_url='" + image_url + '\'' +
                ", image_status=" + image_status +
                ", create_time=" + create_time +
                '}';
    }
}
