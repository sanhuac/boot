public class UnifiedException extends Exception {

    public UnifiedException() {
    }

    public UnifiedException(int code) {
        super(String.valueOf(code));
    }

    public UnifiedException(String message) {
        super(message);
    }

    public UnifiedException(String message, Throwable cause) {
        super(message, cause);
    }

    public UnifiedException(Throwable cause) {
        super(cause);
    }

    public UnifiedException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
